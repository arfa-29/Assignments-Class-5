# Write lambda functions for:

# Squaring a number
square = lambda a :(a**2)
print(square(3))

# Multiplying two numbers
multiply = lambda a,b:(a*b)
print(multiply(5,6))

# Checking if a number is positive
num_positive = lambda number: "Positive Number" if number >=0 else "Negative Number"
print(num_positive(-6))
